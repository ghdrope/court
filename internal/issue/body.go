package issue

import (
	"fmt"
	"strings"

	"github.com/ghdrope/court/internal/incident"
)

// buildBody generates a detailed Markdown report for the VCS issue.
//
// The output is designed for:
//   - human debugging
//   - Copilot / LLM-assisted diagnosis
//   - structured incident analysis
func buildBody(inc *incident.IncidentReport) string {

	// --- Events section ---
	var warnings []string
	var normalEvents []string

	for _, e := range inc.Events {

		line := fmt.Sprintf(
			"- [%s] %s: %s",
			e.Type,
			e.Reason,
			e.Message,
		)

		if strings.EqualFold(e.Type, "warning") {
			warnings = append(warnings, line)
		} else {
			normalEvents = append(normalEvents, line)
		}
	}

	eventsSection := joinSections(
		renderSection("Warnings", warnings),
		renderSection("Events", normalEvents),
	)

	// --- Container diagnostics ---
	var containers []string

	for _, c := range inc.ContainerIssues {

		logs := formatLogs(c.Logs)

		entry := fmt.Sprintf(
			"### Container `%s`\n"+
				"- Image: `%s`\n"+
				"- Failure Reason: `%s`\n\n"+
				"#### Logs\n"+
				"```\n%s\n```",
			c.Container,
			c.ImageName,
			c.Reason,
			logs,
		)

		containers = append(containers, entry)
	}

	body := fmt.Sprintf(`
## 🚨 Automated Incident Report

This issue was automatically generated by **[Court](https://github.com/ghdrope/court)** based on a detected runtime failure.

---

## 📌 Summary

A failure was detected in a Kubernetes workload.  
This issue contains diagnostic data to help identify root cause and guide remediation.

---

## 🧭 Environment

- **Cluster:** %s
- **Namespace:** %s
- **Pod:** %s

---

## ⚠️ Failure Signals

%s

---

## 📦 Container Analysis

%s

---

## 🧩 Potential Root Cause Signals

- Primary failure signal: %s
- Likely causes: crash loops, misconfiguration, missing dependencies, resource exhaustion

---

## 🧠 Suggested Investigation

Please review the following:

1. Container exit reason and logs
2. Recent changes to the workload (image, config, deploys)
3. Dependency availability (external services, databases, APIs)
4. Resource constraints (CPU, memory, limits)
5. Kubernetes events for scheduling or runtime errors

`,
		inc.Cluster,
		inc.Namespace,
		inc.Pod,
		eventsSection,
		strings.Join(containers, "\n\n"),
		extractPrimaryReason(inc),
	)

	body += vcsSectionFor(inc)

	body += `

---

_This issue was generated automatically by [Court](https://github.com/ghdrope/court)._
`

	return body
}
